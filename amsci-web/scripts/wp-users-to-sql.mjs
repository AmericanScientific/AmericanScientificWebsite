// Transform the WordPress user export → D1 seed SQL for the `users` table.
//
//   node scripts/wp-users-to-sql.mjs                 # reads private/wp-users-export.json
//   node scripts/wp-users-to-sql.mjs > private/users-seed.sql
//
// Migrates all accounts. `status` is preserved verbatim, so 'pending'/'denied'
// accounts are login-blocked by the API until approved. Passwords stay in their
// original WordPress hash ($P$ / $wp$) and are verified + lazily upgraded on
// first login. Everyone starts at price_level 1 (base); real NetSuite tier
// resolution is layered in later. No hashes/PII are printed to the console.
import { readFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const IN = resolve(here, "../private/wp-users-export.json");

function sqlStr(v) {
	if (v === null || v === undefined || v === "") return "NULL";
	return "'" + String(v).replace(/'/g, "''") + "'";
}

/** WP 'YYYY-MM-DD HH:MM:SS' (UTC) → ISO 'YYYY-MM-DDTHH:MM:SSZ'; fallback for blanks. */
function toIso(wpDate, fallback) {
	if (typeof wpDate === "string" && /^\d{4}-\d{2}-\d{2} \d{2}:\d{2}:\d{2}$/.test(wpDate) && !wpDate.startsWith("0000")) {
		return wpDate.replace(" ", "T") + "Z";
	}
	return fallback;
}

const data = JSON.parse(readFileSync(IN, "utf8"));
const fallbackTs = data.exported_at || "2026-01-01T00:00:00Z";
const rows = data.users || [];

const seen = new Set();
let emitted = 0;
let skippedNoEmail = 0;
let skippedDup = 0;

const lines = [];
lines.push("-- Generated by scripts/wp-users-to-sql.mjs — DO NOT COMMIT (contains PII).");
lines.push("-- Idempotent: INSERT OR IGNORE keys on the unique email index.");
lines.push("BEGIN TRANSACTION;");

for (const u of rows) {
	const email = (u.email || "").trim().toLowerCase();
	if (!email) {
		skippedNoEmail++;
		continue;
	}
	if (seen.has(email)) {
		skippedDup++;
		continue;
	}
	seen.add(email);

	const isAdmin = u.role === "administrator" ? 1 : 0;
	const created = toIso(u.registered, fallbackTs);
	const values = [
		sqlStr(email), // email
		u.wp_user_id ? String(u.wp_user_id) : "NULL", // wp_user_id
		sqlStr(u.display_name || ""), // display_name
		"NULL", // password_hash (modern; set on first login)
		sqlStr(u.password_hash), // wp_password_hash
		sqlStr(u.status), // status (null = legacy → treated approved)
		sqlStr(u.role || "customer"), // role
		String(isAdmin), // is_admin
		"1", // price_level (base for now)
		"NULL", // netsuite_customer_id
		sqlStr(created), // created_at
		sqlStr(created), // updated_at
	];
	lines.push(
		"INSERT OR IGNORE INTO users " +
			"(email, wp_user_id, display_name, password_hash, wp_password_hash, status, role, is_admin, price_level, netsuite_customer_id, created_at, updated_at) VALUES (" +
			values.join(",") +
			");",
	);
	emitted++;
}

lines.push("COMMIT;");

process.stdout.write(lines.join("\n") + "\n");
process.stderr.write(`users: emitted ${emitted}, skipped ${skippedNoEmail} (no email), ${skippedDup} (dup email)\n`);
